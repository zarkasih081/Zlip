function t(key, fallback = null) {
  const lang = (typeof GlobalState !== 'undefined' && GlobalState.lang) ? GlobalState.lang : 'en';
  const trans = I18N[lang] || I18N.en;
  
  if(trans[key]) return trans[key];
  if(fallback) return fallback;
  
  if(lang !== 'en' && I18N.en[key]) {
    return I18N.en[key];
  }
  
  return key.replace(/_/g, ' ').toUpperCase();
}

function applyLanguage() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const trans = t(el.getAttribute('data-i18n'));
    if (trans.includes('<')) {
      el.innerHTML = trans;
    } else {
      el.textContent = trans;
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.setAttribute('placeholder', t(el.getAttribute('data-i18n-placeholder')));
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.setAttribute('title', t(el.getAttribute('data-i18n-title')));
  });
  // Update lucide icons inside elements that were overwritten
  if (window.lucide) {
    lucide.createIcons();
  }
}

// Function to handle language change from settings UI
function updateDynamicTexts() {
  if (typeof _spElimMode !== 'undefined' && document.getElementById('elimToggleText')) {
    const el = document.getElementById('elimToggleText');
    el.innerHTML = _spElimMode ? (t('sp_elim_on') || '<i data-lucide="crosshair" style="width:16px"></i> Eliminasi: AKTIF') : (t('sp_elim_off') || '<i data-lucide="crosshair" style="width:16px"></i> Eliminasi: Nonaktif');
    if (window.lucide) lucide.createIcons({ root: el });
  }
  if (typeof renderFavorites === 'function') renderFavorites();
  if (typeof renderGlobalHistory === 'function') renderGlobalHistory();
  if (typeof renderTimer === 'function') renderTimer();
}

function changeLang(newLang) {
  if (GlobalState.lang !== newLang) {
    GlobalState.lang = newLang;
    if (typeof saveGlobalState === 'function') saveGlobalState();
    applyLanguage();
    updateDynamicTexts();
  }
}
